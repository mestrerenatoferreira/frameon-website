function injectCSS(){
  if(document.getElementById('i18n-style'))return;
  const s=document.createElement('style');s.id='i18n-style';s.textContent=STYLE;
  document.head.appendChild(s);
}

function getLang(){ return localStorage.getItem('fo_lang')||'pt'; }

window.setLang=function(lang){
  if(!T[lang])return;
  localStorage.setItem('fo_lang',lang);
  apply(lang);
  document.querySelectorAll('.lang-btn').forEach(b=>b.classList.toggle('active',b.dataset.lang===lang));
  document.documentElement.lang=lang==='pt'?'pt-BR':lang;
  // Show/hide legal notice for non-PT on legal pages
  const notice=document.getElementById('legal-notice');
  if(notice){
    notice.textContent=T[lang].legal_notice||'';
    notice.classList.toggle('visible',lang!=='pt'&&!!(T[lang].legal_notice));
  }
};

function apply(lang){
  const tr=T[lang]||T.pt;
  window._fo_tr=tr; // expose for modal JS
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const v=tr[el.dataset.i18n];if(v!==undefined)el.textContent=v;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el=>{
    const v=tr[el.dataset.i18nHtml];if(v!==undefined)el.innerHTML=v;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el=>{
    const v=tr[el.dataset.i18nPh];if(v!==undefined)el.placeholder=v;
  });
  // Select options
  document.querySelectorAll('select[name=colaboradores]').forEach(sel=>{
    const opts=sel.querySelectorAll('option');
    const keys=['modal_opt_select','modal_opt_upto50','modal_opt_50200','modal_opt_2001k','modal_opt_1kplus'];
    opts.forEach((o,i)=>{if(tr[keys[i]])o.textContent=tr[keys[i]];});
  });
}

document.addEventListener('DOMContentLoaded',function(){
  injectCSS();
  const lang=getLang();
  apply(lang);
  document.querySelectorAll('.lang-btn').forEach(b=>b.classList.toggle('active',b.dataset.lang===lang));
  document.documentElement.lang=lang==='pt'?'pt-BR':lang;
  const notice=document.getElementById('legal-notice');
  if(notice&&lang!=='pt'&&T[lang].legal_notice){
    notice.textContent=T[lang].legal_notice;notice.classList.add('visible');
  }
});

